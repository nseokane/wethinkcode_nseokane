#include <stdio.h>

int ft_is_prime(int nb){
    int i = 2, prime = 1;

    while (i <= (nb / 2)){
        if (nb % i == 0){
            prime = 0;
            break;
        }
        i++;
    }

    if (prime == 1){
        return 1;
    }else{
        return 0;
    }
}

