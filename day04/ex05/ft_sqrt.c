int ft_sqrt(int nb) {
    int i = 1, x = nb, z = 1, yes;

    while ((z * z) <= nb){
        if ((nb % z == 0) && (nb / z == z)){
            yes = 1;
        }
        z++;
    }

    if (yes == 1){
        while (x > i){
            x = (x + i) / 2;
            i = nb / x;
        }
        return x; 
    }else{
        return 0;
    }
}